#define CACHE_SIZE      1024
#define RED     "\033[1;31m"
#define ORANGE  "\033[01;33m"
#define WHITE   "\033[37m"
#define RESET   "\033[0m"

LexerStream lexer_stream_open(
    const char *file_path)
{
    LexerStream stream;
    stream.file = fopen(file_path, "rb");
    if (!stream.file)
    {
        printf(RED "Error: could not open file '%s'\n", 
            file_path);
        
        stream.eof_flag = 1;
        stream.cache = NULL;
        return stream;
    }

    stream.eof_flag = 0;
    stream.index = 0;
    stream.line_index = 0;
    stream.column = 0;
    stream.line = 0;
    stream.cache = (char*)malloc(CACHE_SIZE);

    return stream;
}

void lexer_stream_close(
    LexerStream *stream)
{
    if (stream->file)
        fclose(stream->file);
    
    if (stream->cache)
        free(stream->cache);
}

char lexer_read_char(
    LexerStream *lex, 
    int index)
{
    fseek(lex->file, index, SEEK_SET);
    return fgetc(lex->file);
}

char *lexer_read_buffer(
    LexerStream *lex,
    int index, 
    int len)
{
    fseek(lex->file, index, SEEK_SET);
    fread(lex->cache, sizeof(char), len + 1, lex->file);
    return lex->cache;
}

void lexer_read_string(
    LexerStream *lex, 
    Token token, 
    char *out)
{
    char *buffer;

    buffer = lexer_read_buffer(lex, 
        token.data_index, token.len);
    memcpy(out, buffer, token.len);
    out[token.len] = '\0';
}

char *lexer_read_line(
    LexerStream *lex, 
    int index, 
    int *len)
{
    *len = 0;
    while (lexer_read_char(lex, index + *len) != '\n')
        *len += 1;
    
    return lexer_read_buffer(lex, index, *len);
}

void lexer_error(
    LexerStream *lex,
    Token token,
    const char *error)
{
    int len, i;
    char *line;

    printf(RED "Error(%i:%i): %s\n", 
        token.line_no + 1, token.column_no,
        error);

    line = lexer_read_line(lex, token.line_index, &len);
	printf("\t" WHITE);
    fwrite(line, sizeof(char), len, stdout);

    printf("\n\t");
    for (i = 0; i < token.column_no; i++)
        printf(" ");

    printf(ORANGE);
    for (i = 0; i < token.len; i++)
        printf("~");
    printf("\n" RESET);
}

